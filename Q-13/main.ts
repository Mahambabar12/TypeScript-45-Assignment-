// Q-13 Your Own Array: Think of your favorite mode of transportation,
//  such as a motorcycle or a car, and make a list that stores several examples. 
//  Use your list to print a series of statements about these items, such
//   as “I would like to own a Honda motorcycle.”

let transportation:string[] = ["Bike", "car", "bus", "bicycle"];

for (let i = 0; i < transportation.length; i++) {
    console.log (`I would like to won a ${transportation[i]}`);
    
}

//Answer
// I would like to won a Bike
// I would like to won a car
// I would like to won a bus
// I would like to won a bicycle